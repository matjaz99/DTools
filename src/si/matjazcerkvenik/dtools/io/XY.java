package si.matjazcerkvenik.dtools.io;

/**
 * This class contains X and Y coordinates (integers) for generating charts.
 * 
 * @author matjaz
 *
 */
public class XY {
	
	private int x;
	private int y;
	
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	
	
}
